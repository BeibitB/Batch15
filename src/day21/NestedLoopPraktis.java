package day21;

public class NestedLoopPraktis {

    public static void main(String[] args) {

        for (int i = 0; i <4 ; i++) {
            System.out.println(i);

            for (int x = 0; x < 5 ; x++) {
                System.out.print(x );
            }

        }
        System.out.println();



    }
}
