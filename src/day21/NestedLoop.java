package day21;

public class NestedLoop {
    public static void main(String[] args) {

        for (int x = 0; x < 4 ; x++) {
            System.out.println("" + x);


        for (int i = 1; i <= 10 ; i++) {
            if ( i % 2 ==0) {
                System.out.print(i);
            }
            }
            System.out.println();
        }

    }
}
