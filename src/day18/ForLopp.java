public class ForLopp {
    public static void main(String[] args) {

        for (int i = 0 ; i <= 10 ; ++i){
            System.out.print(i);
        }
        System.out.println();
        for (int i = 0; i < 100; i++) {
            if ( i % 2 == 1)
                System.out.print(i + " ");
        }



    }
}
