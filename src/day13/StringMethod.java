package day13;

import java.util.Scanner;

public class StringMethod {
    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);
        float x = scan.nextFloat();
        float y = 10/x;
        System.out.println(x);
        System.out.println(y);
        System.out.println(--x);
        System.out.println(--y);
    }
}

